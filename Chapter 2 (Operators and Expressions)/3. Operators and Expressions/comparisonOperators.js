// Comparison Operators

let comp1 = 6;
let comp2 = "6";

console.log("comp1==comp2 is ", comp1 == comp2); // == (Double = means, it checks the values are equal or not)
console.log("comp1!=comp2 is ", comp1 != comp2);

console.log("comp1===comp2 is ", comp1 === comp2); // === (Triple = means, it checks the value as well as type are equal or  not)
console.log("comp1!==comp2 is ", comp1 !== comp2);