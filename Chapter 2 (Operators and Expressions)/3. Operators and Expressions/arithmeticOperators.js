//Operators

let a = 10;
let b = 4;

console.log("a+b = ", a+b); // Add
console.log("a-b = ", a-b); // Subtract
console.log("a/b = ", a/b); // Divide
console.log("a*b = ", a*b); // Multiply
console.log("a**b = ", a**b); // Power of
console.log("a%b = ", a%b); // Modulus, Gives the remainder after division
console.log("a++ = ", a++); // Post Increment = 10
console.log("++a = ", ++a); // Pre Increment = 12
console.log("a-- = ", a--); // Post Decrement = 12
console.log("--a = ", --a); // Pre Decrement = 10