// Explore includes, startswith and endswith function a string

// All return boolean

let names = "Talha";

//Includes
if(names.includes("Tal")){
    console.log("Present");
}
else{
    console.log("Not Present");
}

// StartsWith

if(names.startsWith("T")){
    console.log("True");
}
else{
    console.log("False");
}

// EndsWith

if(names.endsWith("l")){
    console.log("True");
}
else{
    console.log("False");
}